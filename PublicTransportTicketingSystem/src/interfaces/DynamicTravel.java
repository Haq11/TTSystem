    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        Routes route = (Routes) jComboBox1.getSelectedItem();
        ArrayList<String> stops = route.getStops();
        DefaultTableModel model1 = (DefaultTableModel) Table01.getModel();
        model1.setNumRows(0);
        DefaultTableModel model2 = (DefaultTableModel) Table02.getModel();
        model2.setNumRows(0);
        for (String stop : stops) {
            model1.addRow(new Object[]{stop});
            model2.addRow(new Object[]{stop});
        }
    }//GEN-LAST:event_jButton7ActionPerformed

