    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Routes route = (Routes) jComboBox1.getSelectedItem();
        ArrayList<String> stops = route.getStops();
        DefaultTableModel model1 = (DefaultTableModel) Table01.getModel();
        model1.setNumRows(0);
        for (String stop : stops) {
            if (stop.startsWith(jTextField2.getText())) {
                model1.addRow(new Object[]{stop});
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

